import math; # Tem as funções mais difíceis da matemática

def Soma(num1, num2):
    soma = num1 + num2
    print(f"A Soma dos Números: {soma}")

def Subtração(num1, num2):
    subtração = num1 - num2
    print(f"A Subtração dos Números: {subtração}")

def Multiplicacao(num1, num2):
    Multiplicacao = num1 * num2
    print(f"A Multiplicação dos Números: {Multiplicacao}")

def Divisao(num1, num2):
    if num1 == 0 or num2 == 0:
        print("Não é Possível o Dividendo ou o Divisor igual a 0")
    else:
        Divisao = num1 // num2
        print(f"A Divisão dos Números: {Divisao}")




 